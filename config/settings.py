def _env_bool(name, default=False):
    raw = os.getenv(name)
    if raw is None:
        return default
    return raw.strip().lower() in {'1', 'true', 'yes', 'on'}


def _env_list(name, default=None):
    raw = os.getenv(name, '')
    items = [item.strip() for item in raw.split(',') if item.strip()]
    return items if items else (default or [])


