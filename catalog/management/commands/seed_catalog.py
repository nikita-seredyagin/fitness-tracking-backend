MUSCLE_GROUPS = [
    'Грудные мышцы', 'Мышцы спины', 'Квадрицепс',
    'Бицепс бедра', 'Ягодицы', 'Икры',
    'Дельтовидные мышцы', 'Бицепс', 'Трицепс', 'Пресс',
]


class Command(BaseCommand):
    help = 'Заполнение базы данных упражнениями и мышечными группами'

    def handle(self, *args, **options):
        MuscleGroup.objects.filter(
            name__in=['Мышцы рук', 'Мышцы ног', 'плечи']
        ).delete()

        groups = {
            name: MuscleGroup.objects.get_or_create(name=name)[0]
            for name in MUSCLE_GROUPS
        }

        for exercise_name, group_roles in EXERCISES:
            exercise, _ = Exercise.objects.get_or_create(name=exercise_name)
            for group_name, is_primary in group_roles:
                ExerciseMuscleGroup.objects.update_or_create(
                    exercise=exercise,
                    muscle_group=groups[group_name],
                    defaults={'is_primary': is_primary},
                )

        self.stdout.write(self.style.SUCCESS(
            f'Готово: {len(groups)} групп, {len(EXERCISES)} упражнений.'
        ))
